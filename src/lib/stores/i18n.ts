const dictionaries: Record<string, Dictionary> = { en, ru };

function format(template: string, vars?: Vars) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ""));
}

export const t = derived(settings, ($settings) => {
  const dictionary = dictionaries[$settings.language] ?? ru;
  return (key: string, vars?: Vars) => format(dictionary[key] ?? en[key] ?? key, vars);
});
